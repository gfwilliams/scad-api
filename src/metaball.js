/*
Author: Gordon Williams, gw@pur3.co.uk

Creates a 3D mesh using metaballs.

Metaball coordinates are specified in normal world coordinates,
but to choose the resolution of the finished model you must
specify `resolution`. If resolution is 4 and you have a metaball
of radius 1, it'll have roughly 8 grid squares (~polygons) across
its width.

If amount of grid squares is too high (>128 in any direction) the grid
will be resized and resolution may not be honoured.

metaball({
  resolution: 4, // grids per unit (optional - default 2)
  border: 1, // grid border (optional - default 1)
  balls : [ // list of balls - x,y,z,radius
    [0,0,0,2],
    [2,0,0,1],
    [-2,0,0,1],
    [0,0,2,0.5],
  ]
}),

The grid is usually made slightly bigger than the metaballs to avoid
overflow - however if you're using a lot of metaballs nearby then
the final size will be larger than the size of the individual balls
and the grid may be too small. If that's the case, specify a larger
`border` value.


Example:

function main() {
   return difference(
       metaball({
           resolution: 4, // grids per unit
           balls : [ // list of balls - x,y,z,radius
         [0,0,0,1.5],
         [2,0,0,1],
         [-2,0,0,1],
         [0,0,2,1],
         [-1,0,2,-1],
         [1,0,2,-1],
       ]}),
       cylinder({start:[0,-5,-0.25],end:[0,5,-0.25],r:1})
    );
}

*/
import { CSG } from '@jscad/csg'
var MAX_SIZE = 128; // maximum grid size

export function metaball(options) {
  options = options||{};
  var balls = options.balls||[];
  if (!balls.length) return CSG.fromPolygons([]);
  var resolution = options.resolution||2;
  var border = options.border||1;
  var min = [100000,100000,100000];
  var max = [-100000,-100000,-100000];
  balls.forEach(function(ball) {
    if (ball[3]===undefined) ball[3]=1; // strength
    var radius = Math.abs(ball[3]);
    for (var i=0;i<3;i++) {
      min[i] = Math.min(min[i], ball[i] - radius);
      max[i] = Math.max(max[i], ball[i] + radius);
    }
  });
  var size = [];
  var maxSize = 0;
  for (var i=0;i<3;i++) {
    max[i] += (2.5+border)/resolution;
    min[i] -= (0.5+border)/resolution;
    size[i] = Math.round((max[i]-min[i])*resolution);
    if (size[i]>maxSize) maxSize=size[i];
  }
  if (maxSize>MAX_SIZE) {
    console.log("Resolution too high! grid resized to "+MAX_SIZE);
    for (var i=0;i<3;i++)
      size[i]=Math.round(size[i] * MAX_SIZE / size[i]); // sanity check
  }

  console.log("metaball grid size: ",size,min,max);
  var vol = { sx:size[0],sy:size[1],sz:size[2] };
  vol.count = vol.sx*vol.sy*vol.sz;
  vol.volume = new Float32Array( vol.count );
  vol.volume.fill(-0.1);
  balls.forEach(function(ball) {
    var p = [];
    for (var i=0;i<3;i++) p[i] = (ball[i]-min[i]) * size[i] / (max[i]-min[i]);
    mbBall(vol, p[0], p[1], p[2], ball[3]*ball[3]*Math.sign(ball[3])*resolution*resolution/10);
  });
  return CSG.fromPolygons( mbTriangulate(vol) )
          .scale([(max[0]-min[0])/size[0],(max[1]-min[1])/size[1],(max[2]-min[2])/size[2]])
          .translate([min[0],min[1],min[2]]);
}

